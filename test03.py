#ทำโปรแกรมป้อนจำนวนเงินและจำนวนทางแป้นพิมพ์และแสดงผลออกมาว่า จำนวนบาท จำนวนคน ต้องหารกันคนละ บาท โดบใช้print("")ทั้ง5แบบ
money = input("ป้อนจำนวนเงิน:")
person = input("ป้อนจำนวนคน:")
final = float(money)/int(person)
print("-----------------------------------------------")
print(f'จำนวนเงิน {money} บาท จำนวนคน {person} คน หารได้คนละ {final:.2f} บาท')   
print('จำนวนเงิน',money,'บาท','จำนวนคน', person,'คน','หารได้คนละ',f'{final:.2f}','บาท')   
print('จำนวนเงิน ' + money +' บาท'+' จำนวนคน '+ person +' คน'+' หารได้คนละ 'f'{final:.2f}'+' บาท')   
print('จำนวนเงิน {} บาท จำนวนคน {} คน หารได้คนละ {:.2f} บาท' .format(money,person,final))   
print('จำนวนเงิน %s บาท จำนวนคน %s คน หารได้คนละ %.2f บาท' %(money,person,final))   